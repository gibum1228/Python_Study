#Day 3 연산자와 형변환 연습하기

a = 15
b = 4

print("%d + %d = %d" %(a, b, a+b)) #덧셈
print("%d - %d = %d" %(a, b, a-b)) #뺄셈
print("%d * %d = %d" %(a, b, a*b)) #곱셈
print("%d / %d = %d" %(a, b, a/b)) #나눗셈 정수
print("%d // %d = %d" %(a, b, a//b)) #나눗셈 정수 몫만 보기
print("%f / %f = %f" %(a, b, a/b)) #나눗셈 실수
print("%f // %f = %f" %(a, b, a//b)) #나눗셈 실수 몫만 보기
print("%d %% %d = %d" %(a, b, a%b)) #나머지
print("%d ** %d = %d" %(a, b, a**b)) #승수